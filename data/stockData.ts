// Helper to get base price from the lookup, with BSE fallback
export const getBasePriceFromData = (symbol: string): number => {
  // Direct lookup
  if (SYMBOL_BASE_PRICES[symbol] !== undefined) {
    return SYMBOL_BASE_PRICES[symbol];
  }
  // BSE symbols: strip .BO suffix and look up NSE equivalent price
  if (symbol.endsWith('.BO')) {
    const nseSymbol = symbol.replace('.BO', '');
    if (SYMBOL_BASE_PRICES[nseSymbol] !== undefined) {
      return SYMBOL_BASE_PRICES[nseSymbol];
    }
  }
  // Default fallback
  return 2500;
};
